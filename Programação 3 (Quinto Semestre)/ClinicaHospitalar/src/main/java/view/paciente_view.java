    private void botao_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_addActionPerformed
        // TODO add your handling code here:
        try {
            if (data_PAC.getText().isEmpty() || idade_PAC.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha os campos obrigatÃ³rios.");
                return;
            }

            Paciente paciente = criarPacienteAPartirCampos();

            if (!id_PAC.getText().isEmpty()) {
                paciente.setId(Long.parseLong(id_PAC.getText()));
                pacienteController.atualizarPaciente(paciente);
                JOptionPane.showMessageDialog(this, "Paciente atualizado com sucesso!");
            } else {
                pacienteController.adicionarPaciente(paciente);
                JOptionPane.showMessageDialog(this, "Paciente cadastrado com sucesso!");
            }

            limparCampos();
            atualizarTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_botao_addActionPerformed

    private void botao_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_editarActionPerformed
        // TODO add your handling code here:
        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um paciente na tabela.");
            return;
        }

        Long id = (Long) jTable1.getValueAt(linha, 0);
        Paciente paciente = pacienteController.buscarPorId(id);

        id_PAC.setText(paciente.getId().toString());
        nome_PAC.setText(paciente.getNomeCompleto());
        data_PAC.setText(paciente.getDataNascimento());
        box_genero_PAC.setSelectedItem(paciente.getGenero().getDescricao());
        tel_PAC.setText(paciente.getContato().getTelefone());
        cel_PAC.setText(paciente.getContato().getCelular());
        email_PAC.setText(paciente.getContato().getEmail());
        rua_PAC.setText(paciente.getEndereco().getRua());
        num_PAC.setText(String.valueOf(paciente.getEndereco().getNumero()));
        bairro_PAC.setText(paciente.getEndereco().getBairro());
        cidade_PAC.setText(paciente.getEndereco().getCidade());
        estado_PAC.setText(paciente.getEndereco().getEstado());
        cep_PAC.setText(paciente.getEndereco().getCep());
        idade_PAC.setText(String.valueOf(paciente.getIdade()));
        data_cadastro_PAC.setText(paciente.getDataCadastro());
        obs_PAC.setText(paciente.getObservacao());
    }//GEN-LAST:event_botao_editarActionPerformed

    private void botao_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_removerActionPerformed
        // TODO add your handling code here:
        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um paciente para remover.");
            return;
        }
        Long id = (Long) jTable1.getValueAt(linha, 0);
        pacienteController.removerPaciente(id);
        atualizarTabela();
        limparCampos();
        JOptionPane.showMessageDialog(this, "Paciente removido com sucesso.");
    }//GEN-LAST:event_botao_removerActionPerformed

