    private void preencherComboGenero() {
        box_genero_PAC.removeAllItems();
        for (Genero genero : Genero.values()) {
            box_genero_PAC.addItem(genero.getDescricao());
        }
    }

    private Paciente criarPacienteAPartirCampos() {
        return new Paciente(
            null,
            nome_PAC.getText(),
            data_PAC.getText(),
            Genero.fromDescricao(box_genero_PAC.getSelectedItem().toString()),
            new ContatoTelemail(
                tel_PAC.getText(),
                cel_PAC.getText(),
                email_PAC.getText()
            ),
            new Endereco(
                rua_PAC.getText(),
                Integer.parseInt(num_PAC.getText()),
                bairro_PAC.getText(),
                cidade_PAC.getText(),
                estado_PAC.getText(),
                cep_PAC.getText()
            ),
            Integer.parseInt(idade_PAC.getText()),
            data_cadastro_PAC.getText(),
            obs_PAC.getText()
        );
    }

    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Paciente pac : pacienteController.listarPacientes()) {
            model.addRow(new Object[]{
                pac.getId(),
                pac.getNomeCompleto(),
                pac.getDataNascimento(),
                pac.getGenero().getDescricao(),
                pac.getContato().getTelefone(),
                pac.getContato().getCelular(),
                pac.getContato().getEmail(),
                pac.getEndereco().getRua(),
                pac.getEndereco().getNumero(),
                pac.getEndereco().getBairro(),
                pac.getEndereco().getCidade(),
                pac.getEndereco().getEstado(),
                pac.getEndereco().getCep(),
                pac.getIdade(),
                pac.getDataCadastro(),
                pac.getObservacao()
            });
        }
    }

