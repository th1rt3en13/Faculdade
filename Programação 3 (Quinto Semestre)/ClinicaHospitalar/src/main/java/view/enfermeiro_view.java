    private void botao_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_addActionPerformed
        // TODO add your handling code here:
        try {
            if (nome_ENF.getText().isEmpty() || ch_ENF.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha os campos obrigatórios.");
                return;
            }

            Enfermeiro enfermeiro = criarEnfermeiroAPartirCampos();

            if (enfermeiro.getId() == null) {
                enfermeiroController.adicionarEnfermeiro(enfermeiro);
                JOptionPane.showMessageDialog(this, "Enfermeiro cadastrado com sucesso!");
            } else {
                enfermeiroController.atualizarEnfermeiro(enfermeiro);
                JOptionPane.showMessageDialog(this, "Enfermeiro atualizado com sucesso!");
            }

             limparCampos();
             atualizarTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }

//GEN-LAST:event_botao_addActionPerformed

    private void botao_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_editarActionPerformed
        // TODO add your handling code here:
        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um enfermeiro na tabela.");
            return;
        }

        Long id = (Long) jTable1.getValueAt(linha, 0);
        Enfermeiro enfermeiro = enfermeiroController.buscarPorId(id);

        id_ENF.setText(enfermeiro.getId().toString());
        nome_ENF.setText(enfermeiro.getNomeCompleto());
        data_ENF.setText(enfermeiro.getDataNascimento());
        box_genero_ENF.setSelectedItem(enfermeiro.getGenero());
        tel_ENF.setText(enfermeiro.getTelefone());
        cel_ENF.setText(enfermeiro.getCelular());
        email_ENF.setText(enfermeiro.getEmail());
        rua_ENF.setText(enfermeiro.getRua());
        num_ENF.setText(String.valueOf(enfermeiro.getNumero()));
        bairro_ENF.setText(enfermeiro.getBairro());
        cidade_ENF.setText(enfermeiro.getCidade());
        estado_ENF.setText(enfermeiro.getEstado());
        cep_ENF.setText(enfermeiro.getCep());
        setor_ENF.setText(enfermeiro.getSetor());
        ch_ENF.setText(String.valueOf(enfermeiro.getCargaHoraria()));
        enf_treinadosimnao.setSelected(enfermeiro.isTreinado());
    }//GEN-LAST:event_botao_editarActionPerformed

    private void botao_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_removerActionPerformed
        // TODO add your handling code here:
        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um enfermeiro para remover.");
            return;
        }
        Long id = (Long) jTable1.getValueAt(linha, 0);
        enfermeiroController.removerEnfermeiro(id);
        atualizarTabela();
        limparCampos();
        JOptionPane.showMessageDialog(this, "Enfermeiro removido com sucesso.");
    }//GEN-LAST:event_botao_removerActionPerformed

