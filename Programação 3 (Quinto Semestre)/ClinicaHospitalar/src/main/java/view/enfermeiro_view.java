    private void preencherComboGenero(){
        box_genero_ENF.removeAllItems(); //limpa existentes
        
        for (Genero genero : Genero.values()){
            box_genero_ENF.addItem(genero.getDescricao());
        }
    }

    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Enfermeiro enf : enfermeiroController.listarEnfermeiros()) {
            model.addRow(new Object[]{
                enf.getId(),
                enf.getNomeCompleto(),
                enf.getDataNascimento(),
                enf.getGenero(),
                enf.getTelefone(),
                enf.getCelular(),
                enf.getEmail(),
                enf.getRua(),
                enf.getNumero(),
                enf.getBairro(),
                enf.getCidade(),
                enf.getEstado(),
                enf.getCep(),
                enf.getSetor(),
                enf.getCargaHoraria(),
                enf.isTreinado() ? "Sim" : "Não"
            });
        }
    }

