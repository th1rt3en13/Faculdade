   private void botao_addActionPerformed(java.awt.event.ActionEvent evt) {
    try {
        Long id;

        if (id_med.getText().isEmpty()) {
            id = (long) (medicoController.listarMedicos().size() + 1);
        } else {
            id = Long.parseLong(id_med.getText());
        }

        id_med.setText(String.valueOf(id)); // Atualiza visualmente

        Medico medico = new Medico(
            id,
            nome_med.getText(),
            data_med.getText(),
            new Endereco(
                rua_med.getText(),
                Integer.parseInt(num_med.getText()),
                bairro_med.getText(),
                cidade_med.getText(),
                estado_med.getText(),
                cep_med.getText()
            ),
            new ContatoTelemail(
                tel_med.getText(),
                cel_med.getText(),
                email_med.getText()
            ),
            Genero.fromDescricao(box_genero_med.getSelectedItem().toString()),
            setor_med.getText(),
            Integer.parseInt(ch_med.getText()),
            Integer.parseInt(crm_med.getText()),
            especialidade_med.getText(),
            checkbox_cirurgiaosimnao.isSelected()
        );

        if (medicoController.buscarPorId(id) == null) {
            medicoController.adicionarMedico(medico);
            JOptionPane.showMessageDialog(this, "Médico cadastrado com sucesso!");
        } else {
            medicoController.atualizarMedico(medico);
            JOptionPane.showMessageDialog(this, "Médico atualizado com sucesso!");
        }

        limparCampos();
        atualizarTabela();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
}


    private void botao_editarActionPerformed(java.awt.event.ActionEvent evt) {
    int linha = jTable1.getSelectedRow();
    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um médico na tabela!");
        return;
    }

    try {
        Long id = (Long) jTable1.getValueAt(linha, 0);
        Medico medico = medicoController.buscarPorId(id);

        if (medico == null) {
            JOptionPane.showMessageDialog(this, "Médico não encontrado.");
            return;
        }

        // Preencher os campos com os dados do médico
        id_med.setText(medico.getId().toString());
        nome_med.setText(medico.getNomeCompleto());
        data_med.setText(medico.getDataNascimento());
        box_genero_med.setSelectedItem(medico.getGenero().getDescricao());
        tel_med.setText(medico.getContato().getTelefone());
        cel_med.setText(medico.getContato().getCelular());
        email_med.setText(medico.getContato().getEmail());
        rua_med.setText(medico.getEndereco().getRua());
        num_med.setText(medico.getEndereco().getNumero().toString());
        bairro_med.setText(medico.getEndereco().getBairro());
        cidade_med.setText(medico.getEndereco().getCidade());
        estado_med.setText(medico.getEndereco().getEstado());
        cep_med.setText(medico.getEndereco().getCep());
        setor_med.setText(medico.getSetor());
        ch_med.setText(medico.getCargaHoraria().toString());
        crm_med.setText(medico.getCrm().toString());
        especialidade_med.setText(medico.getEspecialidade());
        checkbox_cirurgiaosimnao.setSelected(medico.getCirurgiao());

        // ATENÇÃO: não atualizar nada aqui ainda!

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar dados: " + e.getMessage());
    }
}




    private void botao_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_removerActionPerformed
        // TODO add your handling code here:
        int linha = jTable1.getSelectedRow();
        if (linha == -1) return;

        int confirmacao = JOptionPane.showConfirmDialog(
            this, 
            "Tem certeza que deseja remover este médico?", 
            "Confirmação", 
            JOptionPane.YES_NO_OPTION
        );

        if (confirmacao == JOptionPane.YES_OPTION) {
            Long id = (Long) jTable1.getValueAt(linha, 0);
            new MedicoController().removerMedico(id);
            atualizarTabela();
    }
    }//GEN-LAST:event_botao_removerActionPerformed

