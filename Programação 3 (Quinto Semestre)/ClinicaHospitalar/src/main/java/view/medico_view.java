    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpa a tabela

        for (Medico medico : medicoController.listarMedicos()) {
            model.addRow(new Object[]{
                medico.getId(),
                medico.getNomeCompleto(),
                medico.getDataNascimento(),
                medico.getGenero(),
                medico.getContato().getTelefone(),
                medico.getContato().getCelular(),
                medico.getContato().getEmail(),
                medico.getEndereco().getRua(),
                medico.getEndereco().getNumero(),
                medico.getEndereco().getBairro(),
                medico.getEndereco().getCidade(),
                medico.getEndereco().getEstado(),
                medico.getEndereco().getCep(),
                medico.getSetor(),
                medico.getCargaHoraria(),
                medico.getCrm(),
                medico.getEspecialidade(),
                medico.getCirurgiao() ? "Sim" : "Não"
            });
        }
    }

